function FigureFormatFunction


x1hand = get(gca,'xlabel');
y1hand = get(gca,'ylabel');
titlehand = get(gca,'title');


set(x1hand,'FontSize', 10,'FontName','Ariel','FontWeight','bold');
set(y1hand,'FontSize', 10,'FontName','Ariel','FontWeight','bold');
set(titlehand,'FontSize', 12,'FontName','Ariel','FontWeight','bold');


%% Format the axes %%
set(gca,'FontSize',8,'FontWeight','bold','FontName','Ariel')

%% Set figure properties %%
set(gca, ...
    'Box'         , 'on'     , ...
    'TickDir'     , 'in'     , ...
    'TickLength'  , [.01 .01] , ...
    'XMinorTick'  , 'off'      , ...
    'YMinorTick'  , 'off'      , ...
    'YGrid'       , 'on'      , ...
    'Xgrid'       , 'on'      , ...
    'XMinorGrid'  , 'off' , ...
    'YMinorGrid'  , 'off' , ...
    'XColor'      , [0 0 0], ...
    'YColor'      , [0 0 0], ...
    'LineWidth'   , 0.8 ...
    );






